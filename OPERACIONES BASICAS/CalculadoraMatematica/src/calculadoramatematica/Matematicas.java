    void sumar() {
        double num1 = Double.parseDouble(this.num1.getText());
        double num2 = Double.parseDouble(this.num1.getText());
        
        double sumar = num1 + num2;
        
        jLabel4.setText("" + sumar);
    }
    
    void restar() {
        double num1 = Double.parseDouble(this.num1.getText());
        double num2 = Double.parseDouble(this.num1.getText());
        
        double restar = num1 - num2;
        
        jLabel4.setText("" + restar);
    }
    
    void multiplicar() {
        double num1 = Double.parseDouble(this.num1.getText());
        double num2 = Double.parseDouble(this.num1.getText());
        
        double multiplicar = num1 * num2;
        
        jLabel4.setText("" + multiplicar);
    }
    
    void dividir() {
        double num1 = Double.parseDouble(this.num1.getText());
        double num2 = Double.parseDouble(this.num1.getText());
        
        double dividir = num1 / num2;
        
        jLabel4.setText("" + dividir);
    }
    
    void borrar() {
        num1.setText("");
        num2.setText("");
        jLabel4.setText("");
    }    
    
